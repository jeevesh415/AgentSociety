logger = get_logger()


class FunctionWrapper:
    """包装函数信息"""

    def __init__(self, name: str, arguments: str):
        self.name = name
        self.arguments = arguments


class ToolCallWrapper:
    """包装工具调用，统一处理对象和字典格式"""

    def __init__(self, tool_call: Any):
        if isinstance(tool_call, dict):
            self.id = tool_call.get("id", f"call_{uuid.uuid4().hex[:12]}")
            self.type = tool_call.get("type", "function")
            function_data = tool_call.get("function", {})
            self.function = FunctionWrapper(
                name=function_data.get("name", ""),
                arguments=function_data.get("arguments", "{}"),
            )
        else:
            # 已经是对象格式
            self.id = getattr(tool_call, "id", f"call_{uuid.uuid4().hex[:12]}")
            self.type = getattr(tool_call, "type", "function")
            func = getattr(tool_call, "function", None)
            if func:
                self.function = FunctionWrapper(
                    name=getattr(func, "name", ""),
                    arguments=getattr(func, "arguments", "{}"),
                )
            else:
                self.function = FunctionWrapper(name="", arguments="{}")


